public class OperateParam {

    //天线值
    private String ant;
    //衰减增益值
    private String attGain;
    //平均次数值
    private String avg;
    //回传间隔值
    private String interval;
    //显示点数值
    private String points;
    //分辨率值
    private String rbw;
    //起始频率值
    private String startFreq;
    //终止频率值
    private String stopFreq;

    public String getAnt() {
        return ant;
    }

    public void setAnt(String ant) {
        this.ant = ant;
    }

    public String getAttGain() {
        return attGain;
    }

    public void setAttGain(String attGain) {
        this.attGain = attGain;
    }

    public String getAvg() {
        return avg;
    }

    public void setAvg(String avg) {
        this.avg = avg;
    }

    public String getInterval() {
        return interval;
    }

    public void setInterval(String interval) {
        this.interval = interval;
    }

    public String getPoints() {
        return points;
    }

    public void setPoints(String points) {
        this.points = points;
    }

    public String getRbw() {
        return rbw;
    }

    public void setRbw(String rbw) {
        this.rbw = rbw;
    }

    public String getStartFreq() {
        return startFreq;
    }

    public void setStartFreq(String startFreq) {
        this.startFreq = startFreq;
    }

    public String getStopFreq() {
        return stopFreq;
    }

    public void setStopFreq(String stopFreq) {
        this.stopFreq = stopFreq;
    }
}
